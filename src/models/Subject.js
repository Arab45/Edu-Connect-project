const mongoose = require('mongoose');
const Schema = mongoose.Schema

const subjectSchema = new Schema({
    added_by: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    subject_image: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now()
    }
})

module.exports = mongoose.model('Subject', subjectSchema);